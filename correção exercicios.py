'''
Considerando 2 números inteiros, faça a soma, subtração e multiplicação. Por fim, mostre os resultados.
'''

num1 = int(input ("Informe o primeiro número inteiro"))
num2 = int(input("Informe o segundo número inteiro"))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2

print(f"A soma dos dois números é {soma}")
print(f"A subtração dos dois números é {subtracao}")
print(f"A multiplicação dos dois números é {multiplicacao}")


